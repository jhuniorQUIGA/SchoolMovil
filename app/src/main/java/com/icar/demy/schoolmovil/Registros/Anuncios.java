package com.icar.demy.schoolmovil.Registros;

public class Anuncios {

    String anunciosId;
    String anuncios;
    String personalAnuncios;
    public Anuncios(String anunciosId, String anuncios, String personalAnuncios) {
        this.anunciosId = anunciosId;
        this.anuncios = anuncios;
        this.personalAnuncios = personalAnuncios;
    }
    public String getAnunciosId() {
        return anunciosId;
    }

    public String getAnuncios() {
        return anuncios;
    }

    public String getPersonalAnuncios() {
        return personalAnuncios;
    }









}
